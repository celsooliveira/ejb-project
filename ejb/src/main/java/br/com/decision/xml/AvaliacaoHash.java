package br.com.decision.xml;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias(value = "AvaliacaoHash")
public class AvaliacaoHash {

	@XStreamAlias(value = "TurmaId")
	private Integer turmaId;

	@XStreamAlias(value = "AlunoId")
	private Integer alunoId;

	public AvaliacaoHash(final Integer turmaId, final Integer alunoId) {
		this.turmaId = turmaId;
		this.alunoId = alunoId;
	}

	public Integer getTurmaId() {
		return turmaId;
	}

	public void setTurmaId(final Integer turmaId) {
		this.turmaId = turmaId;
	}

	public Integer getAlunoId() {
		return alunoId;
	}

	public void setAlunoId(final Integer alunoId) {
		this.alunoId = alunoId;
	}

}
